document.addEventListener('DOMContentLoaded', function(){


    /** NAVBAR EVENTS */
    const navbar = document.querySelector(".navbar_")
    const botonAbrirNavbar = navbar.querySelector(".abrir_")
    const opcionesNavbar = navbar.querySelector("ul")

    botonAbrirNavbar.addEventListener("click", function(){
        botonAbrirNavbar.classList.toggle("hidden_")
        opcionesNavbar.classList.toggle("show-navbar_")
    })
    
    /** Carrusel de Habilidades */
    






})